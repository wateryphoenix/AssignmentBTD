x = int(input("x ="))
y = int(input("y ="))
def addNumber(x,y):
    print(x,"+",y,"=",x+y)
def subtractNumber(x,y):
    print(x,"-",y,"=",x-y)
def multiplyNumber(x,y):
    print(x,"*",y,"=",x*y)
def devideNumber(x,y):
    print(x,"/",y,"=",x/y)

addNumber(x,y)
subtractNumber(x,y)
multiplyNumber(x,y)
devideNumber(x,y)
